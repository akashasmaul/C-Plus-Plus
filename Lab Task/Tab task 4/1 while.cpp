//using while loop
#include <iostream>
using namespace std;
int main()
{
    int x = 1;
    cout<<"Forward: "<<endl;
    while(x <= 10)
    {
        cout<<x<<endl;
        x++;
    }
    cout<<endl;
    int y = 10;
    cout<<"Backward: "<<endl;
    while(y >= 1)
    {
        cout<<y<<endl;
        y--;
    }

}
